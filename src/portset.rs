/// Single input port wrapper. Use when a Machine has exactly one input port.
///
/// ```ignore
/// impl Machine for Doubler {
///     type Input = In<i32>;
///     type Output = Out<i32>;
///     type Ports = SinglePorts<i32>;
///     // ...
/// }
/// ```
#[derive(Debug, Clone, PartialEq)]
pub struct In<T>(pub T);

impl<T: Send + Clone + 'static> HasPortInfo for In<T> {
    fn port_name(&self) -> &'static str { "input" }
    fn flow_kind(&self) -> FlowKind { FlowKind::Data }
    fn payload_type_id(&self) -> TypeId { TypeId::of::<T>() }
    fn payload_type_name(&self) -> &'static str { core::any::type_name::<T>() }
    fn from_port_name(name: &str, payload: Box<dyn Any + Send>) -> Option<Self> {
        match name {
            "input" => payload.downcast::<T>().ok().map(|v| In(*v)),
            _ => None,
        }
    }
    fn into_any(self) -> Box<dyn Any + Send> { Box::new(self.0) }
}

/// Single output port wrapper. Use when a Machine has exactly one output port.
#[derive(Debug, Clone, PartialEq)]
pub struct Out<T>(pub T);

impl<T: Send + Sync + Clone + 'static> HasPortInfo for Out<T> {
    fn port_name(&self) -> &'static str { "output" }
    fn flow_kind(&self) -> FlowKind { FlowKind::Data }
    fn payload_type_id(&self) -> TypeId { TypeId::of::<T>() }
    fn payload_type_name(&self) -> &'static str { core::any::type_name::<T>() }
    fn from_port_name(name: &str, payload: Box<dyn Any + Send>) -> Option<Self> {
        match name {
            "output" => payload.downcast::<T>().ok().map(|v| Out(*v)),
            _ => None,
        }
    }
    fn into_any(self) -> Box<dyn Any + Send> { Box::new(self.0) }
}

/// PortSet for a single-input, single-output Machine (both Data flow).
///
/// Use with `In<T>` and `Out<T>`.
pub struct SinglePorts<T>(core::marker::PhantomData<T>);

impl<T: Send + Sync + Clone + 'static> PortSet for SinglePorts<T> {
    type Input = In<T>;
    type Output = Out<T>;

    fn port_schema() -> PortSchema {
        PortSchema::new()
            .with(crate::port::PortDecl::input::<T>("input"))
            .with(crate::port::PortDecl::output::<T>("output"))
    }
}

