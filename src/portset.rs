// Empty port sets (for Machines with no input or no output)
// ════════════════════════════════════════════════════════════════════════════

/// Zero-variant output enum: a Machine with no output ports.
/// `ProcessOutput::Yield` can never be constructed for this type.
#[derive(Debug, Clone, PartialEq)]
pub enum NoOutput {}

impl HasPortInfo for NoOutput {
    fn port_name(&self) -> &'static str { match *self {} }
    fn flow_kind(&self) -> FlowKind { match *self {} }
    fn payload_type_id(&self) -> TypeId { match *self {} }
    fn payload_type_name(&self) -> &'static str { match *self {} }
    fn from_port_name(_name: &str, _payload: Box<dyn Any + Send>) -> Option<Self> { None }
    fn into_any(self) -> Box<dyn Any + Send> { match self {} }
}

/// Zero-variant input enum: a Machine with no input ports (pure source).
#[derive(Debug, Clone, PartialEq)]
pub enum NoInput {}

impl HasPortInfo for NoInput {
    fn port_name(&self) -> &'static str { match *self {} }
    fn flow_kind(&self) -> FlowKind { match *self {} }
    fn payload_type_id(&self) -> TypeId { match *self {} }
    fn payload_type_name(&self) -> &'static str { match *self {} }
    fn from_port_name(_name: &str, _payload: Box<dyn Any + Send>) -> Option<Self> { None }
    fn into_any(self) -> Box<dyn Any + Send> { match self {} }
}
